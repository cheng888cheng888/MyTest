package com.ruixue.label.translate;

public class LabelSourceMapper {
	
	private String labelSourceID;
	
	private String labelName;
	
	private String labelDesc;
	
	private String newLabel;


	public String getLabelSourceID() {
		return labelSourceID;
	}

	public void setLabelSourceID(String labelSourceID) {
		this.labelSourceID = labelSourceID;
	}

	public String getLabelName() {
		return labelName;
	}

	public void setLabelName(String labelName) {
		this.labelName = labelName;
	}

	public String getLabelDesc() {
		return labelDesc;
	}

	public void setLabelDesc(String labelDesc) {
		this.labelDesc = labelDesc;
	}

	public String getNewLabel() {
		return newLabel;
	}

	public void setNewLabel(String newLabel) {
		this.newLabel = newLabel;
	}
	

}
