package com.ruixue.label.translate;

public class LabelResult {
	
	private String personID;
	
	private String statPeriod;
	
	private String tagValue;
	
	private String labelDesc;
	
	private String tagValueCode;
	
	private String createTime;

	public String getCreateTime() {
		return createTime;
	}

	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}

	public String getPersonID() {
		return personID;
	}

	public void setPersonID(String personID) {
		this.personID = personID;
	}

	public String getStatPeriod() {
		return statPeriod;
	}

	public void setStatPeriod(String statPeriod) {
		this.statPeriod = statPeriod;
	}

	public String getLabelDesc() {
		return labelDesc;
	}

	public void setLabelDesc(String labelDesc) {
		this.labelDesc = labelDesc;
	}

	public String getTagValue() {
		return tagValue;
	}

	public void setTagValue(String tagValue) {
		this.tagValue = tagValue;
	}

	public String getTagValueCode() {
		return tagValueCode;
	}

	public void setTagValueCode(String tagValueCode) {
		this.tagValueCode = tagValueCode;
	}
	

}
