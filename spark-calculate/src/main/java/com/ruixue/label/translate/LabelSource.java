package com.ruixue.label.translate;

public class LabelSource {
	
	private String labelID;
	
	private String labelName;
	
	private String labelDesc;
	
	private String labelDef;
	
	private String effStartTime;
	
	private String effEndTime;

//	private String effFlag;

	public String getLabelID() {
		return labelID;
	}

	public void setLabelID(String labelID) {
		this.labelID = labelID;
	}

	public String getLabelName() {
		return labelName;
	}

	public void setLabelName(String labelName) {
		this.labelName = labelName;
	}

	public String getLabelDesc() {
		return labelDesc;
	}

	public void setLabelDesc(String labelDesc) {
		this.labelDesc = labelDesc;
	}

	public String getLabelDef() {
		return labelDef;
	}

	public void setLabelDef(String labelDef) {
		this.labelDef = labelDef;
	}

	public String getEffStartTime() {
		return effStartTime;
	}

	public void setEffStartTime(String effStartTime) {
		this.effStartTime = effStartTime;
	}

	public String getEffEndTime() {
		return effEndTime;
	}

	public void setEffEndTime(String effEndTime) {
		this.effEndTime = effEndTime;
	}
	


}
